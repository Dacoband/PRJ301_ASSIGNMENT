package DTO;

/**
 *
 * @author hendrix
 */
public class Admin {

    int admin_id, account_id;
    String name, phone_number, address, email, password, role;
    
    public int getAdmin_id() {
        return admin_id;
    }

    public void setAdmin_id(int admin_id) {
        this.admin_id = admin_id;
    }

    public int getAccount_id() {
        return account_id;
    }

    public void setAccount_id(int account_id) {
        this.account_id = account_id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone_number() {
        return phone_number;
    }

    public void setPhone_number(String phone_number) {
        this.phone_number = phone_number;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    

    public Admin(int admin_id, int account_id, String name, String phone_number, String address, String email, String password, String role) {
        this.admin_id = admin_id;
        this.account_id = account_id;
        this.name = name;
        this.phone_number = phone_number;
        this.address = address;
        this.email = email;
        this.password = password;
        this.role = role;
    }

    public Admin() {
    }

}

